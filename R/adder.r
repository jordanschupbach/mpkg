#' Add Two Numbers
#'
#' This function takes two numeric values and returns their sum.
#'
#' @param a A numeric value to be added.
#' @param b A numeric value to be added.
#' @return The sum of a and b.
#' @examples
#' adder(3, 5) # Returns 8
#' adder(-1, 1) # Returns 0
#' @export
adder <- function(a, b) {
  a + b
}
